var lb = document.getElementById('alt');
var mostra = document.querySelector('.mostra');
lb.addEventListener('click', function() {
    
  if(mostra.style.display === 'block') {
      mostra.style.display = 'none';
  } else {
      mostra.style.display = 'block';
  }
});

